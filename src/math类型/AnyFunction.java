package math类型;

/**
 * 求两数中最大值和最小值和负数的绝对值
 * @author ywx
 * @ date 2018年8月9日
 */
public class AnyFunction {

	public static void main(String[] args) {
		System.out.println("4和8交大者："+ Math.max(4, 8));
		//取两个参数的最小值
		System.out.println("4.4和4较小者："+ Math.min(4.4, 4));
		System.out.println("-7的绝对值："+ Math.abs(-7));
	}

}
